	<h2>Игры Warcraft®</h2>

<p>Последняя новинка Blizzard — World of Warcraft®: Warlords of Draenor®, пятое дополнение к World of Warcraft, ролевой многопользовательской интернет-игре, отмеченной рядом международных премий. Выход Warlords of Draenor состоялся 13 ноября 2014 г. За первые сутки продаж дополнение разошлось тиражом более 3,3 млн экземпляров.</p>

<p>Предыдущее дополнение к World of Warcraft, Mists of Pandaria®, поступило в продажу 25 сентября 2012 г. и за первую неделю разошлось тиражом более 2,7 млн экземпляров. Третье дополнение, Cataclysm®, вышло 7 декабря 2010 г. За первые сутки было продано более 3,3 миллиона экземпляров: Cataclysm стала самой быстропродаваемой игрой для PC в мире... вплоть до выхода другой игры Blizzard Entertainment — Diablo III. До Cataclysm этот рекорд был установлен первыми двумя дополнениями к World of Warcraft, World of Warcraft: The Burning Crusade® и World of Warcraft: Wrath of the Lich King®: за первые 24 часа продаж они разошлись тиражом 2,4 млн и 2,8 млн экземпляров соответственно.</p>

<p>Исходная игра, World of Warcraft, вышла 23 ноября 2004 г. в США, Австралии и Новой Зеландии; затем в 2005 г. в Корее, Европе, Китае, Сингапуре, Тайване, Гонконге и Макао, в 2006 г. в Малайзии, в 2007 г. — в Таиланде, в 2008 г. в России и ряде стран Латинской Америки, а в 2011 г. — в Бразилии. Во всем мире игра имела беспрецедентный успех и сегодня насчитывает миллионы подписчиков. World of Warcraft предлагается на 11 языках, в зависимости от региона. Игра была удостоена ряда наград и снискала множество похвал со стороны критиков.</p>

<p>Hearthstone™: Heroes of Warcraft™ — бесплатная карточная игра Blizzard в жанре «стратегии». В нее можно играть на нескольких платформах: Windows, Mac, iPad и планшетах с Android. Hearthstone — обманчиво простая игра в плане дизайна, но невероятно увлекательная. Она интуитивно понятна всем, а не только тем, кто уже знает настольную карточную игру Warcraft. В декабре 2014 г. вышло первое дополнение к Hearthstone — «Гоблины и гномы»™.</p>

<p>Первую игру Blizzard Entertainment из серии Warcraft, Warcraft: Orcs & Humans™, неоднократно называли лучшей стратегией 1994 г. Ее продолжение, Warcraft II: Tides of Darkness™, (1995 г.), было удостоено многочисленных премий, в том числе первой для компании премии «Игра года». Игра Warcraft III: Reign of Chaos, вышедшая в 2002 г., также была отмечена рядом наград. Наряду с последним на сегодняшний день продолжением, Warcraft III: The Frozen Throne®, (оно вышло в июле 2003 г.) и играми StarCraft, она стала ключевой игрой на профессиональных турнирах видеоигр.</p>

<h2>Игры StarCraft®</h2>

<p>В настоящий момент идет работа над третьей и последней главой в грандиозной трилогии StarCraft II — StarCraft II: Legacy of the Void™. В ней игрокам предстоит объединить разрозненные фракции протоссов в борьбе против древнего зла, которое угрожает гибелью всей галактике. Лишь объединенными усилиями Золотой Армады, могущественной боевой флотилии протоссов, можно остановить натиск тьмы, пока она не поглотила все живое в секторе Копрулу.</p>

<p>12 марта 2013 г. вышло первое дополнение к сверхпопулярной стратегии в реальном времени StarCraft II: Wings of Liberty — StarCraft II: Heart of the Swarm. В нем разворачивается новая кампания, сюжет которой выстроен вокруг Королевы Клинков. Пылая жаждой мести, она стремится снова объединить под своим началом раздробленные войска Роя зергов. В Heart of the Swarm вводится несколько новинок, призванных улучшить самый популярный аспект StarCraft II — соревновательную сетевую игру.</p>

<p>Игра StarCraft II: Wings of Liberty вышла 27 июля 2010 г. и за первые сутки разошлась тиражом более 1 млн, а за первый месяц — более 3 млн экземпляров: это самая быстрораскупаемая стратегия в реальном времени в истории компьютерных игр. Одновременно со StarCraft II была выпущена новая версия игровой платформы Battle.net. Она подверглась основательной переработке и стала для пользователей Blizzard единым сетевым ресурсом нового поколения. Среди новых функций, в частности, был введен голосовой чат, хранение игровых данных на сервере, новая система лиг и рейтингов, достижения, отслеживание статистики и т.д.</p>

<p>Оригинальная игра StarCraft вышла в марте 1998 г. и стала третьим бестселлером Blizzard Entertainment, лидером продаж в 1998 г., по данным PC Data. Дополнение StarCraft: Brood War™ было названо лучшим дополнением 1998 г., и многие специалисты по-прежнему считают его лучшим дополнением к компьютерной игре из ныне существующих.</p>

<p>На профессиональных турнирах компьютерных игр игры серии StarCraft давно и по праву входят в число ключевых дисциплин. В Южной Корее невероятная популярность StarCraft сыграла ключевую роль в развитии киберспорта и профессиональной трансляции матчей, а также в распространении салонов компьютерных игр.</p>

<h2>Игры Diablo®</h2>

<p>Diablo III вышла 15 мая 2012 г. и в первые сутки продаж разошлась тиражом более 35 млн экземпляров: новый абсолютный рекорд продаваемости среди компьютерных игр. Еще более 1,2 млн игроков получили Diablo III в подарок в рамках акции «Годовая подписка на World of Warcraft». В итоге в первую неделю обладателями Diablo III стали более 6,3 млн человек. Это самый яркий результат выпуска игры в истории компьютерных игр.</p> 

<p>3 сентября 2013 г. компания вернулась к разработке консольных игр — поклонники долго этого ждали! Diablo III была адаптирована к Xbox 360 и PlayStation® 3. И профессиональным критикам, и игрокам очень понравились новая система добычи, уровни сложности и другие изменения, внесенные в консольную версию. Многие из этих новинок впоследствии были перенесены и в версию для PC. </p>

<p>Новая глава в грандиозной саге Diablo III — дополнение Reaper of Souls — вышла 25 марта 2014 г. (в версии для Windows и Mac). За неделю игра разошлась тиражом более 2,7 млн экземпляров. Нововведения, которые появились в Diablo III с выходом Reaper of Souls, перешли и в расширенную консольную версию, Ultimate Evil Edition™ (для PlayStation 3, PlayStation® 4, Xbox One и Xbox 360), 19 августа 2014 г.</p>

<p>Первая игра Diablo поступила в продажу в конце декабря 1996 г. и сразу стала лидером продаж в США. По общему мнению, жанр ролевых игр обязан Diablo «вторым рождением». В 2000 г. вышло первое продолжение игры — Diablo II. Оно сразу побило все рекорды по темпам продаж в сфере компьютерных игр. В 2001 г. было выпущено дополнение, Diablo II: Lord of Destruction™, разошедшееся тиражом в более 1 млн экземпляров в первый же месяц продаж и вошедшее в число самых продаваемых игр в 2001 г., не говоря о многочисленных восторженных отзывах критиков.</p>

<h2>Новые игры</h2>

<p>Кроме Warcraft, Diablo и StarCraft, Blizzard Entertainment теперь разрабатывает и другие игровые вселенные — принципиально новые или на основе существующих. Игра Overwatch™, о работе над которой было объявлено на BlizzCon 2014, — командный «шутер» с яркими героями, в котором делается акцент на общедоступность и увлекательность. Действие Overwatch разворачивается в совершенно новом для Blizzard игровом пространстве. Персонажи: солдаты, ученые, искатели приключений и иные колоритные фигуры — сражаются между собой по всему миру в футуристической обстановке.</p>
<p>Еще одна новая игра, которая сейчас находится в разработке, — Heroes of the Storm™: это напряженные командные бои, в которых участвуют культовые персонажи из разных игр Blizzard. Герои и злодеи Азерота, Санктуария, сектора Копрулу в Heroes of the Storm оказываются вовлечены в межпространственное возмущение материй под названием Нексус, где им предстоит сражаться друг с другом командами за славу, за жизнь... и просто забавы ради.</p>
